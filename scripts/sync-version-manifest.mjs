import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const repoRoot = path.resolve(path.dirname(scriptPath), '..');
const manifestPath = path.join(repoRoot, 'version-manifest.json');
const checkOnly = process.argv.includes('--check');

function ensurePositiveInteger(value, name) {
  if (!Number.isInteger(value) || value <= 0) {
    throw new Error(`${name} must be a positive integer`);
  }
  return value;
}

async function readManifest() {
  const raw = await readFile(manifestPath, 'utf8');
  const parsed = JSON.parse(raw);
  return {
    appCompatibilityVersion: ensurePositiveInteger(
      parsed.appCompatibilityVersion,
      'appCompatibilityVersion',
    ),
  };
}

function generatedHeader(commentPrefix) {
  return [
    `${commentPrefix} Code generated by scripts/sync-version-manifest.mjs; DO NOT EDIT.`,
    `${commentPrefix} Source: version-manifest.json`,
    '',
  ].join('\n');
}

function renderOutputs(manifest) {
  const { appCompatibilityVersion } = manifest;
  return [
    {
      path: 'packages/contracts/src/version.generated.ts',
      content: `${generatedHeader('//')}export const APP_COMPATIBILITY_VERSION = ${appCompatibilityVersion};\n`,
    },
    {
      path: 'services/sidecar-go/internal/protocol/version_generated.go',
      content: `package protocol\n\n${generatedHeader('//')}const AppCompatibilityVersion = ${appCompatibilityVersion}\n`,
    },
    {
      path: 'apps/mobile/ios/SyncEngine/LynavoVersion.generated.swift',
      content: `${generatedHeader('//')}let lynavoAppCompatibilityVersion = ${appCompatibilityVersion}\n`,
    },
    {
      path: 'apps/mobile/android/app/src/main/java/com/lynavo/drive/mobile/sync/LynavoVersion.kt',
      content: `package com.lynavo.drive.mobile.sync\n\n${generatedHeader('//')}internal const val APP_COMPATIBILITY_VERSION = ${appCompatibilityVersion}\n`,
    },
  ];
}

async function main() {
  const manifest = await readManifest();
  const outputs = renderOutputs(manifest);
  const stale = [];

  for (const output of outputs) {
    const absolutePath = path.join(repoRoot, output.path);
    if (checkOnly) {
      const existing = await readFile(absolutePath, 'utf8').catch(() => '');
      if (existing !== output.content) {
        stale.push(output.path);
      }
      continue;
    }

    await mkdir(path.dirname(absolutePath), { recursive: true });
    await writeFile(absolutePath, output.content);
  }

  if (stale.length > 0) {
    console.error('Version generated files are out of date:');
    for (const file of stale) {
      console.error(`- ${file}`);
    }
    console.error('Run `pnpm sync:versions` to regenerate them.');
    process.exitCode = 1;
  }
}

await main();
