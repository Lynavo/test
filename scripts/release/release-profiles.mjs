export const REVIEW_API_BASE_URL = 'https://review-api.vividrop.cn';

const TARGETS = new Set(['ios', 'android', 'mac', 'win']);

const RELEASE_PROFILES = Object.freeze({
  'cn-prod': Object.freeze({
    name: 'cn-prod',
    market: 'cn',
    review: false,
    apiBaseUrl: 'https://api.vividrop.cn',
    electronBuilderConfig: 'electron-builder.cn.yml',
    desktopWinScript: 'package:win:cn',
  }),
  'global-prod': Object.freeze({
    name: 'global-prod',
    market: 'global',
    review: false,
    apiBaseUrl: 'https://global-api.vividrop.cn',
    electronBuilderConfig: 'electron-builder.global.yml',
    desktopWinScript: 'package:win:global',
  }),
  'cn-review': Object.freeze({
    name: 'cn-review',
    market: 'cn',
    review: true,
    apiBaseUrl: REVIEW_API_BASE_URL,
    electronBuilderConfig: 'electron-builder.cn.yml',
    desktopWinScript: 'package:win:cn',
  }),
  'global-review': Object.freeze({
    name: 'global-review',
    market: 'global',
    review: true,
    apiBaseUrl: REVIEW_API_BASE_URL,
    electronBuilderConfig: 'electron-builder.global.yml',
    desktopWinScript: 'package:win:global',
  }),
});

export function listReleaseProfileNames() {
  return Object.keys(RELEASE_PROFILES);
}

export function getReleaseProfile(name) {
  const profile = RELEASE_PROFILES[name];
  if (!profile) {
    throw new Error(
      `Unsupported release profile "${name}". Supported profiles: ${listReleaseProfileNames().join(', ')}`,
    );
  }
  return profile;
}

export function parseTargets(value = 'ios,mac,win') {
  const targets = value
    .split(',')
    .map((target) => target.trim())
    .filter(Boolean);

  if (targets.length === 0) {
    throw new Error('At least one release target is required.');
  }

  for (const target of targets) {
    if (!TARGETS.has(target)) {
      throw new Error(
        `Unsupported release target "${target}". Supported targets: ios, android, mac, win`,
      );
    }
  }

  return [...new Set(targets)];
}

export function buildReleasePlan({ profileName, targets }) {
  const profile = getReleaseProfile(profileName);
  assertProfileIntegrity(profile);

  const env = buildProfileEnv(profile);
  const steps = targets.map((target) => buildTargetStep(profile, target));

  return {
    profile,
    env,
    steps,
    mobileReleaseProfileSource: buildMobileReleaseProfileSource(profile),
  };
}

function assertProfileIntegrity(profile) {
  const usesReviewServer = profile.apiBaseUrl === REVIEW_API_BASE_URL;
  if (profile.review && !usesReviewServer) {
    throw new Error(`${profile.name} must use ${REVIEW_API_BASE_URL}.`);
  }
  if (!profile.review && usesReviewServer) {
    throw new Error(
      `${profile.name} is a production profile and cannot use ${REVIEW_API_BASE_URL}.`,
    );
  }
}

function buildProfileEnv(profile) {
  return {
    SYNCFLOW_RELEASE_PROFILE: profile.name,
    SYNCFLOW_MARKET: profile.market,
    SYNCFLOW_API_BASE_URL: profile.apiBaseUrl,
    VIVIDROP_API_BASE_URL: profile.apiBaseUrl,
    SYNCFLOW_GIFTCARD_REDEEM_BASE_URL: profile.apiBaseUrl,
    SYNCFLOW_AUTH_BASE_URL: profile.apiBaseUrl,
    SYNCFLOW_CLIENT_CONFIG_BASE_URL: profile.apiBaseUrl,
    ELECTRON_BUILDER_CONFIG: profile.electronBuilderConfig,
  };
}

function buildTargetStep(profile, target) {
  if (target === 'ios') {
    return {
      target,
      command: 'bash',
      args: ['apps/mobile/ios/scripts/testflight-release.sh', 'archive-upload', profile.market],
    };
  }
  if (target === 'android') {
    return {
      target,
      command: 'bash',
      args: [
        '-lc',
        `cd apps/mobile/android && ./gradlew assemble${capitalize(profile.market)}Release`,
      ],
    };
  }
  if (target === 'mac') {
    return {
      target,
      command: 'pnpm',
      args: ['package:desktop:signed'],
    };
  }
  return {
    target,
    command: 'pnpm',
    args: ['--filter', '@syncflow/desktop', profile.desktopWinScript],
  };
}

function capitalize(value) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}

function buildMobileReleaseProfileSource(profile) {
  return `// Generated by scripts/release/release.mjs. Do not edit by hand.
export const mobileReleaseProfile = {
  name: '${profile.name}',
  market: '${profile.market}',
  review: ${profile.review},
  apiBaseUrl: '${profile.apiBaseUrl}',
} as const;

export const releaseApiBaseUrl = mobileReleaseProfile.apiBaseUrl.trim() || null;
`;
}
